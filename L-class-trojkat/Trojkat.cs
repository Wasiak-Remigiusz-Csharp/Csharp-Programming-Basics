public class Trojkat
{
    // dane
    public double a; // pole -> boki trojkata
    private double b; // pole -> boki trojkata
    private double _c; // pole -> boki trojkata


    // kontruktor -> operator "new"

    public Trojkat()
    {
        a = 1;
        b = 1;
        _c = 1;
    }

    public Trojkat(double a, double bb, double c)
    {
        // this.a = a;
        // this.b = b;
        // this.c = c;
        this.a = a;
        b = bb;
        _c = c;
    }

    // metody  --> zachowanie trojkata
}